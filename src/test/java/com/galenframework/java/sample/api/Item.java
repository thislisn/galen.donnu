package com.galenframework.java.sample.api;

public class Item {
    private String Title;
    private String Description;
    private String ContentTileDescription;
    private String Url;
    private String ItemPath;
    private String ImageUrl;
    private String ProductSearchImageUrl;
    private String DataID;
    private String ProductName;

    public String getTitle() {
        return Title;
    }

    public String getDescription() {
        return Description;
    }

    public String getContentTileDescription() {
        return ContentTileDescription;
    }

    public String getUrl() {
        return Url;
    }

    public String getItemPath() {
        return ItemPath;
    }

    public String getImageUrl() {
        return ImageUrl;
    }

    public String getProductSearchImageUrl() {
        return ProductSearchImageUrl;
    }

    public String getDataID() {
        return DataID;
    }

    public String getProductName() {
        return ProductName;
    }
}
